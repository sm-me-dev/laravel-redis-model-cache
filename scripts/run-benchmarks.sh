#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
REPORT_DIR="$ROOT_DIR/docs/benchmarks"
REPORT="$REPORT_DIR/report.md"
TIMESTAMP=$(date "+%Y-%m-%d %H:%M:%S")

mkdir -p "$REPORT_DIR"

# Environment info
PHP_VERSION=$(php -v | head -1)
OS_INFO=$(uname -a)
REDIS_VERSION=$(redis-cli INFO server 2>/dev/null | grep redis_version | cut -d: -f2 || echo 'N/A')
LARAVEL_VERSION=$(php -r "require '$ROOT_DIR/vendor/autoload.php'; \$app = require_once '$ROOT_DIR/workbench/bootstrap/app.php'; \$app->make(Illuminate\Contracts\Console\Kernel::class)->bootstrap(); echo app()->version();" 2>/dev/null || echo 'N/A')

cat > "$REPORT" << EOF
# Benchmark Report

**Generated:** $TIMESTAMP
**Environment:** $OS_INFO
**PHP:** $PHP_VERSION
**Laravel:** $LARAVEL_VERSION
**Redis:** $REDIS_VERSION

EOF

run_benchmark() {
    local label="$1"
    local script="$2"
    local extra_args="${3:-}"
    local script_path="$ROOT_DIR/$script"

    {
        echo "## $label"
        echo ""
        echo '```'
    } >> "$REPORT"

    if [ -f "$script_path" ]; then
        if php "$script_path" $extra_args 2>&1; then
            :
        else
            echo "Benchmark exited with code $?" 
        fi
    else
        echo "Benchmark script not found: $script_path"
    fi >> "$REPORT"

    {
        echo '```'
        echo ""
    } >> "$REPORT"
}

run_benchmark "Write Throughput" "benchmarks/throughput_benchmark.php" "--scale=100"
run_benchmark "Latency Percentiles" "benchmarks/latency_benchmark.php" "--operations=500"
run_benchmark "Memory Usage" "benchmarks/memory_benchmark.php"
run_benchmark "Incremental vs Full Store" "benchmarks/incremental_update_benchmark.php"

{
    echo ""
    echo "---"
    echo ""
    echo "*Report generated by scripts/run-benchmarks.sh*"
} >> "$REPORT"

echo "Benchmark report written to $REPORT"
